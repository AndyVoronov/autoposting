class MoscowFactsSource:
    def __init__(self):
        self.fact_types = ["history", "architecture", "unknown"]

    async def generate_fact(
        self,
        fact_type: Optional[str] = None,
        use_ai: bool = True,
    ) -> dict:
        if not fact_type:
            fact_type = random.choice(self.fact_types)

        if fact_type not in MOSCOW_FACTS_DB:
            fact_type = random.choice(self.fact_types)

        fact_data = random.choice(MOSCOW_FACTS_DB[fact_type])

        if use_ai:
            ai_prompt = f"""Ты ведешь Telegram канал о Москве для широкой аудитории. Напиши интересный факт о Москве.

Тема: {fact_data["topic"]}
Контекст: {fact_data["context"]}

Требования:
- Напиши увлекательно и понятно для всех
- Добавь конкретные детали (даты, имена, цифры, адреса)
- Длина 150-250 слов
- Используй эмодзи уместно (2-3 штуки)
- НЕ используй Markdown (жирный, курсив), только обычный текст
- В конце добавь хештеги: #Москва #ИнтересныеФакты #MoscowFacts

Текст:"""

            generated = await ai_service.generate_text(
                prompt=ai_prompt,
                max_tokens=500,
                temperature=0.9,
            )

            if generated:
                body = generated.strip()
            else:
                body = f"{fact_data['context']}\n\n#Москва #ИнтересныеФакты #MoscowFacts"
        else:
            body = f"{fact_data['context']}\n\n#Москва #ИнтересныеФакты #MoscowFacts"

        return {
            "type": fact_type,
            "topic": fact_data["topic"],
            "body": body,
            "generated_with_ai": use_ai,
            "generated_at": datetime.utcnow(),
        }

    async def generate_multiple(
        self,
        count: int = 3,
        use_ai: bool = True,
    ) -> list[dict]:
        facts = []

        for i in range(count):
            fact_type = self.fact_types[i % len(self.fact_types)]
            fact = await self.generate_fact(fact_type=fact_type, use_ai=use_ai)
            facts.append(fact)

        return facts


moscow_facts_source = MoscowFactsSource()
